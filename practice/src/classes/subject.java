package classes;

public class subject {
	String subjectName;
	int score;
	
	
	
	public subject(String name) {
		subjectName=name;
		
	}
	
	public String getSubjectName() {
		return subjectName;
	}
	
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	
	public int getScore() {
		return score;
	}
	
	public void setScore(int score) {
		this.score = score;
	}
	
	





}
